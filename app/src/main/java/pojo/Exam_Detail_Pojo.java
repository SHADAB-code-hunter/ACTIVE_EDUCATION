public class Exam_Detail_Pojo  {
    private String id;
    private String name;
    private String e_seats;
    private String exam_date;
    private String e_info;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getE_seats() {
        return e_seats;
    }

    public void setE_seats(String e_seats) {
        this.e_seats = e_seats;
    }

    public String getExam_date() {
        return exam_date;
    }

    public void setExam_date(String exam_date) {
        this.exam_date = exam_date;
    }

    public String getE_info() {
        return e_info;
    }

    public void setE_info(String e_info) {
        this.e_info = e_info;
    }



}
